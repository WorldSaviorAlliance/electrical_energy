package com.warrior.eem.entity.constant;

/**
 * 用户状态:激活/禁用 
 * @author cold_blade
 */
public enum UserStatus {

	ACTIVE("active"), DISABLE("disable");
	
	private UserStatus(String desc) {
		this.desc = desc;
	}
	
	public String getDesc() {
		return desc;
	}
	
	@Override
	public String toString() {
		return desc;
	}
	
	private String desc;
}
